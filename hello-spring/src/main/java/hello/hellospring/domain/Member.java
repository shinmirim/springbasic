package hello.hellospring.domain;

public class Member {

    //시스템이 저장하는 id 식별자 요구사항

    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
